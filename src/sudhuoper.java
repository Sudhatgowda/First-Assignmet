public class sudhuoper {
    public static void main(String[] args) {
        int a = 5;
        ++a;          // a becomes 6
        a++;          // a becomes 7
        --a;          // a becomes 6
        a--;          // a becomes 5

        System.out.println(++a);

        int i = 5, j;
        j = i++;

        System.out.println(i);
        System.out.println(j);


    }
}
